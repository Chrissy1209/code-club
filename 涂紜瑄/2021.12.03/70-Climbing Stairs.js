var climbStairs = function(n) {
    if(n<=2){return n;}
    var x=1;
    var y=2;
    var result=0;
    if(n>=3){
        for(let i=2;i<n;i++){
            result=x+y;
            x=y;
            y=result;
        }
        return result;
    }
};